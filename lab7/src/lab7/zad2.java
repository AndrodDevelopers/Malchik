package lab7;

public class zad2 {

	public static void main(String[] args) {
		{
			System.out.println("for");
			int i;
		for(i=25; i<=35; i++, System.out.println())
			System.out.printf("%d %.1f %.1f", i,i+0.5,i-0.2);
		}
		int i=25;
		System.out.println("while");
		while(i<=35){
			System.out.printf("%d %.1f %.1f", i,i+0.5,i-0.2);
			System.out.println();
		i++;
		}
			System.out.println("do while");
			i=25;
			do
			{
				System.out.printf("%d %.1f %.1f", i,i+0.5,i-0.2);
				System.out.println();
				i++;
			}
			while(i<=35);
			}
		}


